import _mongoose, { connect } from "mongoose";

// const MONGODB_URI = "mongodb://localhost:27017/portfolio";
const MONGODB_URI = "mongodb://172.17.0.2:27017/portfolio";

if (!MONGODB_URI || MONGODB_URI.length === 0) {
  throw new Error("Please add your MongoDB URI to .env.local");
}

/**
 * Global is used here to maintain a cached connection across hot reloads
 * in development. This prevents connections from growing exponentially
 * during API Route usage.
 */
let cached = global.mongoose;

if (!cached) {
  cached = global.mongoose = { conn: null, promise: null };
}

async function dbConnect() {
  if (cached.conn) {
    console.log("üöÄ Using cached connection");
    return cached.conn;
  }

  if (!cached.promise) {
    const opts = {
      bufferCommands: false,
      dbName: "portfolio",
      useNewUrlParser: true,
      useUnifiedTopology: true,
    };

    cached.promise = connect(MONGODB_URI, opts)
      .then((mongoose) => {
        console.log("‚úÖ New connection established");
        return mongoose;
      })
      .catch((error) => {
        console.error("‚ùå Connection to database failed");
        console.error(error);
        throw error;
      });
  }

  try {
    cached.conn = await cached.promise;
  } catch (e) {
    cached.promise = null;
    throw e;
  }

  return cached.conn;
}

export default dbConnect;
